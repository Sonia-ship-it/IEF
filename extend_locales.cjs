function deepMerge(target, source) {
  for (const key of Object.keys(source)) {
    if (
      source[key] &&
      typeof source[key] === 'object' &&
      !Array.isArray(source[key]) &&
      target[key] &&
      typeof target[key] === 'object'
    ) {
      deepMerge(target[key], source[key]);
    } else {
      target[key] = source[key];
    }
  }
  return target;
}

for (const lang of ['en', 'fr', 'rw']) {
  const filePath = path.join(localesDir, `${lang}.json`);
  const data = JSON.parse(fs.readFileSync(filePath, 'utf8'));
  const ext = extensions[lang];

  // Apply fix keys for French encoding issues
  if (lang === 'fr') {
    if (ext.orderConfFix) {
      Object.assign(data.orderConf, ext.orderConfFix);
      delete ext.orderConfFix;
    }
    if (ext.howtoshopFix) {
      Object.assign(data.howtoshop, ext.howtoshopFix);
      delete ext.howtoshopFix;
    }
    if (ext.adminFix) {
      Object.assign(data.admin, ext.adminFix);
      delete ext.adminFix;
    }
  }

  deepMerge(data, ext);
  fs.writeFileSync(filePath, JSON.stringify(data, null, 2) + '\n', 'utf8');
  console.log(`Updated ${lang}.json`);
}
